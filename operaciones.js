export const suma = (a, b) => a + b

//sintaxis identificador
export const resta = function(a,b){
    return a - b
}

//sintaxis legada
export function multiplicacion(a, b){
    return a * b
}

export function division(a, b){
    return a / b
}

export function potencia(a, b){
    return a ** b
}

